package di.uniba.map.b.lab.generics;

/**
 *
 * @author pierpaolo
 */
public class GenericMethods {

    /**
     *
     * @param <T>
     * @param x
     */
    public <T> void f(T x) {
        System.out.println(x.getClass().getName());
    }

    /**
     *
     * @param args
     */
    public static void main(String[] args) {
        GenericMethods gm = new GenericMethods();
        gm.f("");
        gm.f(1);
        gm.f(1.0);
        gm.f(1.0F);
        gm.f('c');
        gm.f(gm);
    }

}
